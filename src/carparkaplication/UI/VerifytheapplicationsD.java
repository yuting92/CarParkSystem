    @Action
    public void ok_btm_clicked() {
        if(jList1.getSelectedIndex()!=-1)
        {
            StaffObj theStaffObj=applicationStaffList.get(jList1.getSelectedIndex());
            ((Administrator)theMainFrame.theUser).theCarPark_Control.makePlate(theStaffObj.getStaff_number(), theStaffObj.getPlate(), true);
            iniContent();
        }else{
            MessageDialog myMessageDialog=new MessageDialog(theMainFrame, rootPaneCheckingEnabled);
            myMessageDialog.setMessage("Please select a item !");
            myMessageDialog.setVisible(true);
        }
    }

    @Action
    public void no_btn_clicked() {
        if(jList1.getSelectedIndex()!=-1)
        {
            StaffObj theStaffObj=applicationStaffList.get(jList1.getSelectedIndex());
            ((Administrator)theMainFrame.theUser).theCarPark_Control.makePlate(theStaffObj.getStaff_number(), theStaffObj.getPlate(), false);
            iniContent();
        }else{
            MessageDialog myMessageDialog=new MessageDialog(theMainFrame, rootPaneCheckingEnabled);
            myMessageDialog.setMessage("Please select a item !");
            myMessageDialog.setVisible(true);
        }
    }

