    public void iniContent()
    {
        applicationStaffList.clear();
        applicationStaffList=((Administrator)theMainFrame.theUser).theCarPark_Control.getUnStaff();
        List<String> stafflist=new ArrayList<String>();
        for(int i=0;i<applicationStaffList.size();i++)
        {
            StaffObj theStaffObj=applicationStaffList.get(i);
            String itemS="  "+theStaffObj.getStaff_number()+"                      "+theStaffObj.getDepartment()+"                     "+theStaffObj.getPlate();
            stafflist.add(itemS);
        }
        jList1.setListData(stafflist.toArray());
    }
